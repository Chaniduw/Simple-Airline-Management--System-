    private void DisplayFlights(){
    
        try {
            
            Con=DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb","root","");
            stmt=Con.createStatement();
            rs=stmt.executeQuery("select * from flighttbl");
            FlightTbl.setModel(DbUtils.resultSetToTableModel(rs));
            
             
        } catch (Exception e) {
        }
    
    }
    
    int FId=0;
    
    private void countFlights()
    {
    
        try {
            stmt2=Con.createStatement();
            rs1=stmt2.executeQuery("select max(Flcode)from flighttbl");
            rs1.next();
            FId=rs1.getInt(1)+1;
            
        } catch (Exception e) {
            
        }
        
    
    }
    
