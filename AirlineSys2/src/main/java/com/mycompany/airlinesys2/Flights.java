    private void SaveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveBtnMouseClicked
        
        if(Fcode.getText().isEmpty()||Fseats .getText().isEmpty()||Fsource.getSelectedIndex() == -1||Fdest.getSelectedIndex()==-1||Fseats.getText().isEmpty())
        {
          
            JOptionPane.showMessageDialog(this,"Missing Information");
         
        
        }else{
            
            
            try {
                
                countFlights();
                Connection Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb","root","");
                PreparedStatement Add=Con.prepareStatement("insert into flighttbl  values (?,?,?,?,?)");
                Add.setString(1,Fcode.getText());
                Add.setString(2,Fsource.getSelectedItem().toString());
                Add.setString(3,Fdest.getSelectedItem().toString());
                Add.setString(4,Fdate.getDate().toString());
                Add.setInt(5, Integer.valueOf(Fseats.getText()));
                int row =Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Flight Added Successfully !!!");
                Con.close();
                DisplayFlights();
                clear();
                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e);
            }
            
            
        
        
        
        }
        
        
        
        
    }//GEN-LAST:event_SaveBtnMouseClicked
    
